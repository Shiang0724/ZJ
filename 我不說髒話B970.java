package ZeroJudge;

import java.util.Scanner;

public class 我不說髒話B970 {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		while(sc.hasNext()) {
			int n=sc.nextInt();
			int i=1;
			while(i<=n) {
				System.out.println(i+". I don't say swear words!");
				i++;
			}
		}

	}

}
